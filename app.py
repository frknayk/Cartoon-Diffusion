if input_prompt is not None:
    if st.button("Generate Image"):
        with st.spinner("Generating the image..."):
            image_output = generate_images_stable_diffusion(input_prompt, model_path)
            st.image(image_output, caption="Generated by cartoon-blip-captions-model")
